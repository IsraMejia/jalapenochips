include <p16f877.inc>

CBLOCK .32
ORG 00H

BCF STATUS,RP1 //BANCOS DE MEMORIA
BSF STATUS,RP0

MOVLW D'255' //W->255
MOVWF TRISB  //TRISB->0XFF = (255)d, conf a  ENTRADA    
MOVWF TRISC  //TRISB->0XFF = (255)d ENTRADA  
CLRF TRISD  //TRISD->0X00 = (0)d SALIDA
CLRF TRISA  //TRISA->0X00 = (0)d SALIDA

MOVLD D'6' //W->6
MOVWF ADCON1 //ADCON1->6 

BCF STATUS,RP0 // limpia el bit 5 (RP0) del registro STATUS.

